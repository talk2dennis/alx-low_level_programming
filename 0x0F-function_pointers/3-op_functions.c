#include "3-calc.h"

/**
 * op_add - function to add two int
 * @a: int a
 * @b: int b
 * Return: returns the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to sub two numbers
 * @a: first number
 * @b: second number
 * Return: the difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to return the product of two numbers
 * @a: first number
 * @b: second number
 * Return: the product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - fuction to calculate the div of two numbers
 * @a: first number
 * @b: second number
 * Return: the div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to return the mod of two numbers
 * @a: first number
 * @b: second number
 * Return: the mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
